static void render_status(void) {
    render_qmk_logo();
    oled_write_P(PSTR("Kyria rev1.0\n\n"), false);

    oled_write_P(PSTR("Layer: "), false);
    switch (get_highest_layer(layer_state)) {
        case COLEMAK:
            oled_write_P(PSTR("Colemak\n\n"), false);
            break;
        case NUMBER:
            oled_write_P(PSTR("Num / Symbols\n\n"), false);
            break;
        case FUNCTION:
            oled_write_P(PSTR("Function\n\n"), false);
            break;
        default:
            oled_write_P(PSTR("Undefined\n\n"), false);
    }

    uint8_t led_usb_state = host_keyboard_leds();
    oled_write_P(IS_LED_ON(led_usb_state, USB_LED_CAPS_LOCK) ? PSTR("CAPS LOCK\n ") : PSTR("\n"), false);
}

void oled_task_user(void) {
    if (is_keyboard_master()) {
        render_status();
    } else {
        render_kyria_logo();
    }
}
